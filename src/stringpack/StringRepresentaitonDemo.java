package stringpack;

public class StringRepresentaitonDemo {

	public static void main(String[] args) {
		
		//Actual Representation of the String
		String str1 = new String("Arun");
		
		//Shortcut Representation of the String
		String str2 = "Arun Motoori";
		
		System.out.println(str1);
		System.out.println(str2);

	}

}
